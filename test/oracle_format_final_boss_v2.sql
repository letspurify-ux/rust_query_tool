               WHERE b.emp_id = e.emp_id
                 AND b.bonus_year = 2024
           ) AS bonus_sum,
           DENSE_RANK() OVER
           (
               PARTITION BY e.dept_id
               ORDER BY
                   (
                       SELECT SUM(b2.amount)
                       FROM qt_fmt_bonus b2
                       WHERE b2.emp_id = e.emp_id
                   ) DESC NULLS LAST,
                   e.emp_id
           ) AS bonus_rank_in_dept,
           CASE
               WHEN REGEXP_LIKE(DBMS_LOB.SUBSTR(e.note_text, 300, 1), '\(|\)') THEN
                   CASE
                       WHEN REGEXP_LIKE(DBMS_LOB.SUBSTR(e.note_text, 300, 1), 'comment', 'i') THEN 'PAREN_AND_COMMENT_WORD'
                       ELSE 'PAREN_ONLY'
                   END
               WHEN REGEXP_LIKE(DBMS_LOB.SUBSTR(e.note_text, 300, 1), ';') THEN 'SEMICOLON'
               ELSE 'NORMAL'
           END AS note_class,
           REGEXP_REPLACE(
               REPLACE(
                   REPLACE(DBMS_LOB.SUBSTR(e.note_text, 120, 1), CHR(10), ' '),
                   CHR(13),
                   ' '
               ),
               '\s+',
               ' '
           ) AS preview_text,
           (
               SELECT LISTAGG(t.k || ':' || t.v, ' | ') WITHIN GROUP (ORDER BY t.k)
               FROM TABLE(qt_fmt_pkg.get_pairs(e.emp_id)) t
           ) AS pair_payload,
           CASE
               WHEN e.salary >= 100000
                    AND EXISTS
                        (
                            SELECT 1
                            FROM qt_fmt_bonus bx
                            WHERE bx.emp_id = e.emp_id
                              AND bx.amount >= 10000
                        ) THEN
                   CASE
                       WHEN a.grade = 'A' THEN 'ELITE_A'
                       WHEN a.grade = 'B' THEN 'ELITE_B'
                       ELSE 'ELITE_OTHER'
                   END
               WHEN e.salary >= 80000 THEN
                   CASE
                       WHEN a.remote_yn = 'true' THEN 'MID_REMOTE'
                       WHEN a.remote_yn = 'false' THEN 'MID_OFFICE'
                       ELSE 'MID_UNKNOWN'
                   END
               ELSE
                   CASE
                       WHEN EXISTS
                            (
                                SELECT 1
                                FROM qt_fmt_bonus byy
                                WHERE byy.emp_id = e.emp_id
                                  AND byy.bonus_type IN ('SPOT', 'STOCK')
                            ) THEN 'LOW_SPECIAL'
                       ELSE 'LOW_BASIC'
                   END
           END AS deep_case_label
    FROM ebase e
    JOIN qt_fmt_dept d
      ON d.dept_id = e.dept_id
    LEFT JOIN r
      ON r.dept_id = d.dept_id
    LEFT JOIN agg a
      ON a.emp_id = e.emp_id
) q
ORDER BY
       CASE
           WHEN q.deep_case_label LIKE 'ELITE%' THEN 1
           WHEN q.deep_case_label LIKE 'MID%'   THEN 2
           ELSE 3
       END,
       q.bonus_sum DESC NULLS LAST,
       q.emp_id;
/

