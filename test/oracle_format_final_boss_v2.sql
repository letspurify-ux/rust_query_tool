-- VIEW WITH NESTED CTE + JSON_TABLE + XMLTABLE + ANALYTIC + SCALAR SUBQUERY
--------------------------------------------------------------------------------
CREATE OR REPLACE VIEW qt_fmt_emp_v
AS
    WITH dept_path
         (
             dept_id,
             parent_dept_id,
             dept_path_txt,
             lvl
         )
         AS
         (
             SELECT d.dept_id,
                    d.parent_dept_id,
                    TO_CHAR(d.dept_name) AS dept_path_txt,
                    1                    AS lvl
             FROM qt_fmt_dept d
             WHERE d.parent_dept_id IS NULL
             UNION ALL
             SELECT c.dept_id,
                    c.parent_dept_id,
                    p.dept_path_txt || ' > ' || c.dept_name,
                    p.lvl + 1
             FROM qt_fmt_dept c
             JOIN dept_path p
               ON p.dept_id = c.parent_dept_id
         ),
         skill_rows
         AS
         (
             SELECT e.emp_id,
                    jt.skill,
                    jt.grade,
                    jt.remote_yn
             FROM qt_fmt_emp e,
                  JSON_TABLE
                  (
                      e.json_doc,
                      '$'
                      COLUMNS
                      (
                          grade      VARCHAR2(10)  PATH '$.meta.grade',
                          remote_yn  VARCHAR2(5)   PATH '$.meta.remote',
                          NESTED PATH '$.skills[*]'
                              COLUMNS
                              (
                                  skill VARCHAR2(100) PATH '$'
                              )
                      )
                  ) jt
         ),
         xml_rows
         AS
         (
             SELECT e.emp_id,
                    x.flag_txt
             FROM qt_fmt_emp e,
                  XMLTABLE
                  (
                      '/emp/flags/flag'
                      PASSING e.xml_doc
                      COLUMNS flag_txt VARCHAR2(100) PATH '.'
                  ) x
         )
    SELECT e.emp_id,
           e.emp_name,
           e.login_name,
           e.email_addr,
           e.salary,
           e.bonus_pct,
           e.hire_dt,
           e.status_cd,
           e.job_title,
           d.dept_id,
           d.dept_code,
           d.dept_name,
           dp.dept_path_txt,
           (
               SELECT LISTAGG(sr.skill, ', ') WITHIN GROUP (ORDER BY sr.skill)
               FROM skill_rows sr
               WHERE sr.emp_id = e.emp_id
           ) AS skill_list,
           (
               SELECT LISTAGG(xr.flag_txt, ', ') WITHIN GROUP (ORDER BY xr.flag_txt)
               FROM xml_rows xr
               WHERE xr.emp_id = e.emp_id
           ) AS flag_list,
           (
               SELECT SUM(b.amount)
               FROM qt_fmt_bonus b
               WHERE b.emp_id = e.emp_id
                 AND b.bonus_year = 2024
           ) AS bonus_2024,
           ROW_NUMBER() OVER
           (
               PARTITION BY e.dept_id
               ORDER BY e.salary DESC NULLS LAST, e.emp_id
           ) AS rn_in_dept,
           DENSE_RANK() OVER
           (
               ORDER BY e.salary DESC NULLS LAST
           ) AS salary_rank_all,
           AVG(e.salary) OVER
           (
               PARTITION BY e.dept_id
           ) AS dept_avg_salary,
           SUM(
               CASE
                   WHEN e.status_cd = 'ACTIVE' THEN NVL(e.salary, 0)
                   ELSE 0
               END
           ) OVER
           (
               PARTITION BY e.dept_id
           ) AS active_salary_sum
    FROM qt_fmt_emp e
    JOIN qt_fmt_dept d
      ON d.dept_id = e.dept_id
    LEFT JOIN dept_path dp
      ON dp.dept_id = d.dept_id;
/

