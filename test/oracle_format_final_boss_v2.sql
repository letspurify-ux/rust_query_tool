-- MONSTER QUERY #2
-- PIVOT + UNPIVOT + MODEL + GROUPING SETS
--------------------------------------------------------------------------------
WITH bonus_base
AS
(
    SELECT b.emp_id,
           b.bonus_year,
           b.bonus_type,
           b.amount
    FROM qt_fmt_bonus b
    WHERE b.bonus_year = 2024
),
bonus_pivoted
AS
(
    SELECT *
    FROM bonus_base
    PIVOT
    (
        SUM(amount)
        FOR bonus_type IN
        (
            'BASE'  AS base_amt,
            'SPOT'  AS spot_amt,
            'STOCK' AS stock_amt
        )
    )
),
bonus_unpivoted
AS
(
    SELECT emp_id,
           metric_name,
           metric_value
    FROM bonus_pivoted
    UNPIVOT INCLUDE NULLS
    (
        metric_value
        FOR metric_name IN
        (
            base_amt  AS 'BASE_AMT',
            spot_amt  AS 'SPOT_AMT',
            stock_amt AS 'STOCK_AMT'
        )
    )
),
salary_model
AS
(
    SELECT dept_id,
           emp_id,
           salary,
           0 AS projected_salary
    FROM qt_fmt_emp
    MODEL
        PARTITION BY (dept_id)
        DIMENSION BY (emp_id)
        MEASURES (salary, projected_salary)
        RULES
        (
            projected_salary[ANY] =
                NVL(salary[CV(emp_id)], 0)
                * CASE
                      WHEN NVL(salary[CV(emp_id)], 0) >= 100000 THEN 1.10
                      WHEN NVL(salary[CV(emp_id)], 0) >=  80000 THEN 1.07
                      ELSE 1.03
                  END
        )
)
SELECT
       COALESCE(TO_CHAR(e.dept_id), 'ALL_DEPT') AS dept_id_txt,
       COALESCE(e.status_cd, 'ALL_STATUS')      AS status_cd,
       SUM(e.salary)                            AS total_salary,
       AVG(sm.projected_salary)                 AS avg_projected_salary,
       MAX(CASE WHEN bu.metric_name = 'BASE_AMT'  THEN bu.metric_value END) AS max_base_amt,
       MAX(CASE WHEN bu.metric_name = 'SPOT_AMT'  THEN bu.metric_value END) AS max_spot_amt,
       MAX(CASE WHEN bu.metric_name = 'STOCK_AMT' THEN bu.metric_value END) AS max_stock_amt
FROM qt_fmt_emp e
LEFT JOIN salary_model sm
  ON sm.emp_id = e.emp_id
LEFT JOIN bonus_unpivoted bu
  ON bu.emp_id = e.emp_id
GROUP BY GROUPING SETS
(
    (e.dept_id, e.status_cd),
    (e.dept_id),
    (e.status_cd),
    ()
)
ORDER BY
       dept_id_txt,
       status_cd;
/

