SELECT
       e.emp_id,
       e.emp_name,
       x.top_bonus_type,
       x.top_bonus_amt,
       y.skill_cnt,
       y.first_skill
FROM qt_fmt_emp e
     OUTER APPLY
     (
         SELECT b.bonus_type AS top_bonus_type,
                b.amount     AS top_bonus_amt
         FROM qt_fmt_bonus b
         WHERE b.emp_id = e.emp_id
         ORDER BY b.amount DESC NULLS LAST, b.bonus_type
         FETCH FIRST 1 ROW ONLY
     ) x
     OUTER APPLY
     (
         SELECT COUNT(*)      AS skill_cnt,
                MIN(jt.skill) AS first_skill
         FROM JSON_TABLE
              (
                  e.json_doc,
                  '$.skills[*]'
                  COLUMNS
                  (
                      skill VARCHAR2(100) PATH '$'
                  )
              ) jt
     ) y
ORDER BY e.emp_id;
