CREATE OR REPLACE PACKAGE BODY qt_fmt_pkg
AS
    FUNCTION get_pairs
    (
        p_emp_id IN NUMBER
    )
        RETURN qt_fmt_pair_tab PIPELINED
    AS
    BEGIN
        FOR r IN
        (
            SELECT 'EMP_ID'   AS k, TO_CHAR(e.emp_id) AS v FROM qt_fmt_emp e WHERE e.emp_id = p_emp_id
            UNION ALL
            SELECT 'EMP_NAME' AS k, e.emp_name        AS v FROM qt_fmt_emp e WHERE e.emp_id = p_emp_id
            UNION ALL
            SELECT 'DEPT'     AS k, d.dept_name       AS v
            FROM qt_fmt_emp e
            JOIN qt_fmt_dept d
              ON d.dept_id = e.dept_id
            WHERE e.emp_id = p_emp_id
        )
        LOOP
            PIPE ROW (qt_fmt_pair_ot(r.k, r.v));
        END LOOP;

        RETURN;
    END get_pairs;

    FUNCTION open_monster_cursor
    (
        p_min_salary IN NUMBER,
        p_region_cd  IN VARCHAR2
    )
        RETURN SYS_REFCURSOR
    AS
        l_rc   SYS_REFCURSOR;
    BEGIN
        OPEN l_rc FOR
            WITH base
                 AS
                 (
                     SELECT /*+ MATERIALIZE */
                            e.emp_id,
                            e.emp_name,
                            e.salary,
                            e.status_cd,
                            e.note_text,
                            e.dept_id,
                            d.dept_name,
                            d.region_cd,
                            (
                                SELECT LISTAGG(p.k || '=' || p.v, '; ') WITHIN GROUP (ORDER BY p.k)
                                FROM TABLE(qt_fmt_pkg.get_pairs(e.emp_id)) p
                            ) AS pair_text,
                            (
                                SELECT MAX(level)
                                FROM
                                (
                                    SELECT level
                                    FROM dual
                                    CONNECT BY level <=
                                               (
                                                   SELECT COUNT(*)
                                                   FROM qt_fmt_bonus b
                                                   WHERE b.emp_id = e.emp_id
                                               )
                                )
                            ) AS bonus_depth,
                            CASE
                                WHEN e.salary >= 100000 THEN
                                    CASE
                                        WHEN e.status_cd = 'ACTIVE' THEN 'HIGH_ACTIVE'
                                        ELSE 'HIGH_NOT_ACTIVE'
                                    END
                                WHEN e.salary BETWEEN 80000 AND 99999.99 THEN
                                    CASE
                                        WHEN e.status_cd = 'ACTIVE' THEN 'MID_ACTIVE'
                                        WHEN e.status_cd = 'LEAVE'  THEN 'MID_LEAVE'
                                        ELSE 'MID_OTHER'
                                    END
                                ELSE
                                    CASE
                                        WHEN EXISTS
                                             (
                                                 SELECT 1
                                                 FROM qt_fmt_bonus bx
                                                 WHERE bx.emp_id = e.emp_id
                                                   AND bx.amount > 5000
                                             ) THEN 'LOW_WITH_BONUS'
                                        ELSE 'LOW'
                                    END
                            END AS band_label
                     FROM qt_fmt_emp e
                     JOIN qt_fmt_dept d
                       ON d.dept_id = e.dept_id
                     WHERE e.salary >= p_min_salary
                       AND d.region_cd = p_region_cd
                 ),
                 enriched
                 AS
                 (
                     SELECT b.*,
                            (
                                SELECT SUM(x.amount)
                                FROM qt_fmt_bonus x
                                WHERE x.emp_id = b.emp_id
                            ) AS bonus_sum,
                            ROW_NUMBER() OVER
                            (
                                PARTITION BY b.dept_id
                                ORDER BY b.salary DESC, b.emp_id
                            ) AS rn
                     FROM base b
                 )
            SELECT e.emp_id,
                   e.emp_name,
                   e.dept_name,
                   e.region_cd,
                   e.salary,
                   e.band_label,
                   e.bonus_sum,
                   e.bonus_depth,
                   e.pair_text,
                   (
                       SELECT JSON_ARRAYAGG(
                                  JSON_OBJECT(
                                      'bonusType' VALUE b.bonus_type,
                                      'amount'    VALUE b.amount
                                  )
                              RETURNING CLOB
                              )
                       FROM qt_fmt_bonus b
                       WHERE b.emp_id = e.emp_id
                   ) AS bonus_json,
                   REGEXP_REPLACE(
                       REPLACE(
                           REPLACE(
                               DBMS_LOB.SUBSTR(e.note_text, 200, 1),
                               CHR(10),
                               ' '
                           ),
                           CHR(13),
                           ' '
                       ),
                       '\s+',
                       ' '
                   ) AS note_preview,
                   CASE
                       WHEN e.rn = 1 THEN q'[TOP_IN_DEPT]'
                       ELSE q'[NOT_TOP_IN_DEPT]'
                   END AS dept_flag
            FROM enriched e
            ORDER BY
                   CASE
                       WHEN e.band_label LIKE 'HIGH%' THEN 1
                       WHEN e.band_label LIKE 'MID%'  THEN 2
                       ELSE 3
                   END,
                   e.salary DESC,
                   e.emp_id;

        RETURN l_rc;
    END open_monster_cursor;

    PROCEDURE run_dynamic_merge
    (
        p_bonus_year IN NUMBER
    )
    AS
        l_sql   CLOB;
    BEGIN
        l_sql :=
               q'[
MERGE INTO qt_fmt_audit t
USING
(
    SELECT e.emp_id,
           e.emp_name,
           d.dept_name,
           (
               SELECT SUM(b.amount)
               FROM qt_fmt_bonus b
               WHERE b.emp_id = e.emp_id
                 AND b.bonus_year = :bonus_year
           ) AS bonus_sum
    FROM qt_fmt_emp e
    JOIN qt_fmt_dept d
      ON d.dept_id = e.dept_id
) s
ON (t.module_name = 'qt_fmt_pkg.run_dynamic_merge' AND t.key_text = TO_CHAR(s.emp_id))
WHEN MATCHED THEN
    UPDATE
       SET t.action_name  = 'UPSERT',
           t.payload_text = 'EMP=' || s.emp_name || ';DEPT=' || s.dept_name || ';BONUS=' || TO_CHAR(NVL(s.bonus_sum, 0)),
           t.created_at   = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT
    (
        module_name,
        action_name,
        key_text,
        payload_text
    )
    VALUES
    (
        'qt_fmt_pkg.run_dynamic_merge',
        'INSERT',
        TO_CHAR(s.emp_id),
        'EMP=' || s.emp_name || ';DEPT=' || s.dept_name || ';BONUS=' || TO_CHAR(NVL(s.bonus_sum, 0))
    )
]';

        EXECUTE IMMEDIATE l_sql USING p_bonus_year;
    END run_dynamic_merge;
END qt_fmt_pkg;
