MERGE INTO qt_fmt_bonus t
USING
(
    SELECT e.emp_id,
           2025 AS bonus_year,
           CASE
               WHEN e.salary >= 100000 THEN 'BASE'
               WHEN e.salary >=  80000 THEN 'SPOT'
               ELSE 'BASE'
           END AS bonus_type,
           ROUND(
               NVL(e.salary, 0)
               * CASE
                     WHEN e.status_cd = 'ACTIVE' THEN 0.05
                     ELSE 0.02
                 END,
               2
           ) AS amount,
           'generated by merge' AS calc_note
    FROM qt_fmt_emp e
) s
ON
(
       t.emp_id      = s.emp_id
   AND t.bonus_year  = s.bonus_year
   AND t.bonus_type  = s.bonus_type
)
WHEN MATCHED THEN
    UPDATE
       SET t.amount    = s.amount,
           t.calc_note = s.calc_note || ' / updated'
WHEN NOT MATCHED THEN
    INSERT
    (
        emp_id,
        bonus_year,
        bonus_type,
        amount,
        calc_note
    )
    VALUES
    (
        s.emp_id,
        s.bonus_year,
        s.bonus_type,
        s.amount,
        s.calc_note
    );
