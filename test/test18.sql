MERGE INTO qt_emp tgt
USING (
    SELECT e.emp_id,
        CASE
            WHEN EXISTS (
                SELECT 1
                FROM qt_sales s
                WHERE s.emp_id = e.emp_id
                    AND s.sale_year = 2024
                    AND s.sale_amount > (
                    SELECT AVG (s2.sale_amount)
                    FROM qt_sales s2
                    WHERE s2.dept_id = e.dept_id
                        AND s2.sale_year = 2024
                )
            ) THEN 'ACTIVE'
            ELSE e.status_code
        END AS new_status_code,
        CASE
            WHEN e.salary < 100000 THEN e.salary + 2500
            WHEN e.salary BETWEEN 100000 AND 160000 THEN e.salary + 1500
            ELSE e.salary + 500
        END AS new_salary
    FROM qt_emp e
    WHERE e.status_code IN ('ACTIVE', 'LEAVE')
) src
    ON (tgt.emp_id = src.emp_id)
    WHEN MATCHED THEN
UPDATE
SET tgt.status_code = src.new_status_code,
    tgt.salary = src.new_salary,
    tgt.updated_at = SYSTIMESTAMP
WHERE NVL (tgt.status_code, 'X') <> NVL (src.new_status_code, 'X')
    OR NVL (tgt.salary, - 1) <> NVL (src.new_salary, - 1);
