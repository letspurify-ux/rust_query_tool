        eb.status_code,
        eb.salary_band,
        dt.dept_code,
        dt.dept_name,
        dt.dept_path,
        sa.sale_year,
        sa.total_sales,
        sa.avg_sales,
        sa.max_sales,
        sa.min_sales,
        (
            SELECT COUNT (*)
            FROM qt_emp c
            WHERE c.mgr_emp_id = eb.emp_id
        ) AS child_cnt,
        (
            SELECT MAX (x.salary)
            FROM qt_emp x
            WHERE x.dept_id = eb.dept_id
                AND x.emp_id <> eb.emp_id
        ) AS dept_other_max_salary
    FROM emp_base eb
    JOIN dept_tree dt
        ON dt.dept_id = eb.dept_id
    LEFT JOIN sales_agg sa
        ON sa.emp_id = eb.emp_id
        AND sa.dept_id = eb.dept_id
)
SELECT
    j.*,
    DENSE_RANK () OVER (PARTITION BY j.sale_year ORDER BY j.total_sales DESC NULLS LAST) AS sales_rank,
    RANK () OVER (PARTITION BY j.dept_code ORDER BY j.salary DESC NULLS LAST) AS salary_rank_in_dept,
    SUM (NVL (j.total_sales, 0)) OVER (PARTITION BY j.dept_code ORDER BY j.sale_year ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS running_dept_sales,
    CASE
        WHEN EXISTS (
            SELECT 1
            FROM qt_sales s2
            WHERE s2.emp_id = j.emp_id
                AND s2.sale_amount > (
                SELECT AVG (s3.sale_amount)
                FROM qt_sales s3
                WHERE s3.dept_id = j.dept_id
                    AND s3.sale_year = j.sale_year
            )
        ) THEN 'ABOVE_DEPT_AVG_EXISTS'
        ELSE 'NO_ABOVE_DEPT_AVG'
    END AS sales_flag
FROM joined_data j
WHERE NVL (j.total_sales, 0) >= (
    SELECT AVG (NVL (x.total_sales, 0))
    FROM joined_data x
    WHERE x.sale_year = j.sale_year
)
ORDER BY j.sale_year,
    sales_rank,
    j.emp_id;
