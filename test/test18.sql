CREATE OR REPLACE VIEW qt_emp_summary_v AS
WITH base_sales AS (
    SELECT
        s.emp_id,
        s.dept_id,
        s.sale_year,
        s.sale_month,
        s.sale_amount,
        SUM (s.sale_amount) OVER (PARTITION BY s.emp_id, s.sale_year ORDER BY s.sale_month) AS running_amt,
        AVG (s.sale_amount) OVER (PARTITION BY s.emp_id, s.sale_year) AS avg_amt
    FROM qt_sales s
),
yearly_sales AS (
    SELECT
        emp_id,
        dept_id,
        sale_year,
        SUM (sale_amount) AS total_amt,
        MAX (running_amt) AS final_running_amt,
        MAX (avg_amt) AS avg_amt
    FROM base_sales
    GROUP BY emp_id,
        dept_id,
        sale_year
)
SELECT
    e.emp_id,
    e.emp_no,
    e.emp_name,
    e.job_title,
    e.status_code,
    d.dept_code,
    d.dept_name,
    y.sale_year,
    y.total_amt,
    y.final_running_amt,
    y.avg_amt,
    DENSE_RANK () OVER (PARTITION BY y.sale_year ORDER BY y.total_amt DESC NULLS LAST) AS sales_rank,
    (
        SELECT COUNT (*)
        FROM qt_emp x
        WHERE x.mgr_emp_id = e.emp_id
    ) AS direct_report_cnt
FROM qt_emp e
JOIN qt_dept d
    ON d.dept_id = e.dept_id
LEFT JOIN yearly_sales y
    ON y.emp_id = e.emp_id;
/

