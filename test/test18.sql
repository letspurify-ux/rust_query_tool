WITH src AS (
    SELECT
        s.emp_id,
        s.sale_year,
        s.sale_month,
        s.sale_amount
    FROM qt_sales s
    WHERE s.sale_year IN (2023, 2024)
),
pivoted AS (
    SELECT *
    FROM src PIVOT (SUM (sale_amount)
    FOR sale_month IN (1 AS M01, 2 AS M02, 3 AS M03, 4 AS M04, 5 AS M05, 6 AS M06))
),
unp AS (
    SELECT
        emp_id,
        sale_year,
        month_code,
        month_amt
    FROM pivoted UNPIVOT INCLUDE NULLS (month_amt
    FOR month_code IN (M01 AS 'M01', M02 AS 'M02', M03 AS 'M03', M04 AS 'M04', M05 AS 'M05', M06 AS 'M06'))
)
SELECT
    u.emp_id,
    e.emp_no,
    e.emp_name,
    u.sale_year,
    u.month_code,
    u.month_amt
FROM unp u
JOIN qt_emp e
    ON e.emp_id = u.emp_id
ORDER BY u.emp_id,
    u.sale_year,
    u.month_code;
