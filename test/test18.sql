SELECT dept_id,
    sale_year,
    sale_month,
    sale_amount,
    projected_amount
FROM (
        SELECT dept_id,
            sale_year,
            sale_month,
            SUM (sale_amount) AS sale_amount,
            CAST (NULL AS NUMBER) AS projected_amount
        FROM qt_sales
        WHERE sale_year = 2024
        GROUP BY dept_id,
            sale_year,
            sale_month
    )
MODEL PARTITION BY (dept_id) DIMENSION BY (sale_month) MEASURES (sale_year, sale_amount, projected_amount) RULES (projected_amount [ 1 ] = sale_amount [ 1 ] * 1.05, projected_amount [ 2 ] = sale_amount [ 2 ] * 1.05, projected_amount [ 3 ] = (sale_amount [ 1 ] + sale_amount [ 2 ] + sale_amount [ 3 ]) / 3, projected_amount [ 4 ] = NVL (projected_amount [ 3 ], sale_amount [ 4 ]) * 1.02)
ORDER BY dept_id,
    sale_month;
