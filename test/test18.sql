                                WHERE EXISTS (
                                        SELECT 1
                                        FROM qt_sales s
                                        WHERE s.emp_id = e.emp_id
                                            AND s.sale_year = 2024
                                    )
                            ) x
                    ) y
            ) z
    )
WHERE dept_rn <= 3
