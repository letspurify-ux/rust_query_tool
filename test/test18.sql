SELECT *
FROM (
        SELECT z.*,
            ROW_NUMBER () OVER (PARTITION BY z.dept_code ORDER BY z.weighted_score DESC NULLS LAST, z.emp_id) AS dept_rn
        FROM (
                SELECT y.emp_id,
                    y.emp_no,
                    y.emp_name,
                    y.dept_code,
                    y.dept_name,
                    y.total_sales,
                    y.avg_sales,
                    y.salary,
                    y.bonus,
                    y.child_cnt,
                    ROUND ((NVL (y.total_sales, 0) * 0.45 + NVL (y.salary, 0) * 0.35 + NVL (y.bonus, 0) * 0.10 + NVL (y.child_cnt, 0) * 1000 * 0.10) / NULLIF (
                        CASE
                            WHEN y.status_code = 'ACTIVE' THEN 1
                            WHEN y.status_code = 'LEAVE' THEN 1.15
                            ELSE 1.30
                        END, 0), 2) AS weighted_score
                FROM (
                        SELECT x.emp_id,
                            x.emp_no,
                            x.emp_name,
                            x.dept_code,
                            x.dept_name,
                            x.status_code,
                            x.salary,
                            x.bonus,
                            x.child_cnt,
                            (
                                SELECT SUM (s1.sale_amount)
                                FROM qt_sales s1
                                WHERE s1.emp_id = x.emp_id
                                    AND s1.sale_year = 2024
                            ) AS total_sales,
                            (
                                SELECT AVG (s2.sale_amount)
                                FROM qt_sales s2
                                WHERE s2.emp_id = x.emp_id
                                    AND s2.sale_year = 2024
                            ) AS avg_sales
                        FROM (
                                SELECT e.emp_id,
                                    e.emp_no,
                                    e.emp_name,
                                    d.dept_code,
                                    d.dept_name,
                                    e.status_code,
