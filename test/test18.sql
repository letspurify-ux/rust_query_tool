CREATE OR REPLACE TRIGGER qt_emp_biud
    BEFORE INSERT OR UPDATE ON qt_emp
    FOR EACH ROW
DECLARE
    v_msg VARCHAR2 (4000);
BEGIN
    IF INSERTING THEN
        IF :NEW.emp_id IS
                NULL THEN
                :NEW.emp_id := qt_seq_emp.NEXTVAL;
        END IF;
            :NEW.created_at := SYSTIMESTAMP;
            v_msg := 'INSERT emp_no=' || :NEW.emp_no || ', dept_id=' || :NEW.dept_id;
    ELSIF UPDATING THEN
            :NEW.updated_at := SYSTIMESTAMP;
            v_msg := 'UPDATE emp_no=' || :OLD.emp_no || ' -> ' || :NEW.emp_no;
    END IF;
        INSERT INTO qt_audit_log (log_level, module_name, message_text, extra_text)
        VALUES ('INFO', 'qt_emp_biud', v_msg, TO_CLOB ('row-level trigger fired'));
END;
